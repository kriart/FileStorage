package web

func translate(language string, key string) string {
	if language != "en" {
		language = "ru"
	}
	if values, ok := translations[language]; ok {
		if value, ok := values[key]; ok {
			return value
		}
	}
	if value, ok := translations["ru"][key]; ok {
		return value
	}
	return key
}

